# Mode of data in R
# Data type of values
# Numeric Values, Character Strings, Logical, Complex Numbers

# How to get mode of a value in variable
variable1 <- "Hello World!"
print(variable1)
mode(variable1)

variable1 <- 10.1 
print(variable1)
mode(variable1)

variable1 <- 10 
print(variable1)
mode(variable1)

variable1 <- 3 == 4 
print(variable1)
mode(variable1)

variable1 <- 3i + 3
print(variable1)
mode(variable1)



